/****************************************************************************
 *
 ****************************************************************************/
extern uint8_t __bss_end__[];

/****************************************************************************
 *
 ****************************************************************************/
static Mutex mutex = MUTEX_CREATE("malloc");
static uint8_t* heap = __bss_end__;

/****************************************************************************
 *
 ****************************************************************************/
void __malloc_lock(struct _reent* _r)
{
   if (interruptsEnabled())
      mutexLock(&mutex, -1);
   else
      kernelLock();
}

/****************************************************************************
 *
 ****************************************************************************/
void __malloc_unlock(struct _reent* _r)
{
   if (interruptsEnabled())
      mutexUnlock(&mutex);
   else
      kernelUnlock();
}

/****************************************************************************
 *
 ****************************************************************************/
ssize_t _write(int fd, const void* buffer, size_t count)
{
   size_t i;

   if ((fd != 1) && (fd != 2))
   {
      errno = EBADF;
      return -1;
   }

   for (i = 0; i < count; i++)
   {
      int c = ((const uint8_t*) buffer)[i];

      if (c == '\n')
         uartTx('\r');

      uartTx(c);
   }

   return (ssize_t) count;
}

/****************************************************************************
 *
 ****************************************************************************/
ssize_t _read(int fd, void* buffer, size_t count)
{
   size_t i;

   if (fd != 0)
   {
      errno = EBADF;
      return -1;
   }

   for (i = 0; i < count; i++)
   {
      int c = uartRx(i ? false : true);

      if (c == EOF)
         break;

      if (c == '\r')
         c = '\n';

      ((uint8_t*) buffer)[i] = (uint8_t) c;
   }

   return (ssize_t) i;
}

/****************************************************************************
 *
 ****************************************************************************/
void* _sbrk(intptr_t increment)
{
   uint8_t* ptr = heap;

   heap += increment;

   if (ptr > (uint8_t*) BOARD_MEM_SIZE)
   {
      puts("out of memory");
      return NULL;
   }

   return ptr;
}

/****************************************************************************
 *
 ****************************************************************************/
int _close(int fd)
{
   switch (fd)
   {
      case 0:
      case 1:
      case 2:
         return 0;
   }

   errno = EBADF;
   return -1;
}

/****************************************************************************
 *
 ****************************************************************************/
int _fstat(int fd, struct stat* ptr)
{
   switch (fd)
   {
      case 0:
      case 1:
      case 2:
         ptr->st_mode = S_IFCHR;
         return 0;
   }

   errno = EBADF;
   return -1;
}

/****************************************************************************
 *
 ****************************************************************************/
off_t _lseek(int fd, off_t offset, int whence)
{
   switch (fd)
   {
      case 0:
      case 1:
      case 2:
         errno = ESPIPE;
         break;

      default:
         errno = EBADF;
   }

   return -1;
}

/****************************************************************************
 *
 ****************************************************************************/
int _isatty(int fd)
{
   switch (fd)
   {
      case 0:
      case 1:
      case 2:
         return 1;
   }

   errno = EBADF;
   return 0;
}

